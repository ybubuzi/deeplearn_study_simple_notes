# ==================== ç»“æœåˆ†æè¯¦è§£ ====================
print("\n4. åˆ†æè®­ç»ƒç»“æœ...")

# è·å–æ¨¡å‹å­¦åˆ°çš„å‚æ•°
weights, bias = model.layers[0].get_weights()
