# ==================== æ¨¡å‹è®­ç»ƒè¯¦è§£ ====================
print("\n3. å¼€å§‹è®­ç»ƒæ¨¡å‹...")

# é‡å¡‘è¾“å…¥æ•°æ®ä¸º2Dæ ¼å¼ï¼ˆTensorFlowè¦æ±‚ï¼‰
X_train_reshaped = X_train.reshape(-1, 1)
